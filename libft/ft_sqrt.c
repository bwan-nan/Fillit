#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int result;

	i = 1;
	result = i * i;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	if (i * i > nb)
		return (0);
	return (1);
}
