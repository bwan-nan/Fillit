#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*b;
	size_t	i;

	i = 0;
	b = ((char *)s);
	while (n > 0)
	{
		b[i] = '\0';
		i++;
		n--;
	}
}
